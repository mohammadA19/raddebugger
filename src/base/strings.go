func CharIsSpace(c uint8) bool {
    return c == ' ' || c == '\n' || c == '\t' || c == '\r' || c == '\f' || c == '\v'
}

func CharIsUpper(c uint8) bool {
    return 'A' <= c && c <= 'Z'
}

func CharIsLower(c uint8) bool {
    return 'a' <= c && c <= 'z'
}

func CharIsAlpha(c uint8) bool {
    return CharIsUpper(c) || CharIsLower(c)
}

func CharIsSlash(c uint8) bool {
    return c == '/' || c == '\\'
}

func CharIsDigit(c uint8, base uint32) bool {
    result := false
    if (0 < base && base <= 16) {
        val := integerSymbolReverse[c]
        if (val < base) {
            result = true
        }
    }
    return result
}

func CharToLower(c uint8) uint8 {
    if (CharIsUpper(c)) {
        c += ('a' - 'A')
    }
    return c
}

func CharToUpper(c uint8) uint8 {
    if (CharIsLower(c)) {
        c += ('A' - 'a')
    }
    return c
}

func CharToCorrectSlash(c uint8) uint8 {
    if (CharIsSlash(c)) {
        c = '/'
    }
    return c
}

